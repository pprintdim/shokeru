/** Переклад значення характеристики (категоріальне → словник; числове → одиниці). */
function trVal(string $v, string $lang): string {
    $v = trim($v);
    if (isset(DICT_VALS[$v])) return DICT_VALS[$v][$lang];
    // "есть (... Lm)" та яскравість
    if (preg_match('/^есть\s*\(/u', $v)) {
        $inner = $v;
        $inner = preg_replace('/^есть/u', $lang==='ua' ? 'є' : 'Yes', $inner);
        $inner = str_replace(['сверхяркий','яркий'], $lang==='ua' ? ['надяскравий','яскравий'] : ['ultra-bright','bright'], $inner);
        return $inner;
    }
    return trUnits($v, $lang);
}

/** Переклад одиниць у числових значеннях. */
function trUnits(string $v, string $lang): string {
    if ($lang === 'ua') {
        $map = [' млн Вольт'=>' млн Вольт',' Вольт'=>' Вольт',' Ватт'=>' Ват',' грамм'=>' г',
                ' месяцев'=>' місяців',' месяца'=>' місяці',' месяц'=>' місяць',' мес'=>' міс',
                ' лет'=>' років',' года'=>' роки',' год'=>' рік',
                ' часов'=>' годин',' часа'=>' години',' час'=>' година',' мин'=>' хв',
                ' в среднем режиме'=>' в середньому режимі',' средний режим'=>' середній режим',
                'ядра'=>'ядра','шт.'=>'шт.','г.'=>'р.'];
    } else {
        $map = [' млн Вольт'=>' mln V',' Вольт'=>' V',' Ватт'=>' W',' грамм'=>' g',' мм'=>' mm',
                ' месяцев'=>' months',' месяца'=>' months',' месяц'=>' month',' мес'=>' mo',
                ' лет'=>' years',' года'=>' years',' год'=>' year',
                ' часов'=>' hours',' часа'=>' hours',' час'=>' hour',' мин'=>' min',
                ' в среднем режиме'=>' (average mode)',' средний режим'=>' average mode',
                'ядра'=>'cores','шт.'=>'pcs','г.'=>'', ' м'=>' m', 'спираль'=>'spiral','х'=>'x'];
    }
    $out = $v;
    foreach ($map as $ru => $tr) $out = str_replace($ru, $tr, $out);
    return trim($out);
}

function trKey(string $k, string $lang): string {
    return DICT_KEYS[trim($k)][$lang] ?? $k;
}
function trCat(string $c, string $lang): string {
    return DICT_CATS[trim($c)][$lang] ?? $c;
}

/** Переклад назви товару (префікс зі словника + модель без змін). */
function trName(string $name, string $lang): string {
    $name = trim($name);
    foreach (DICT_NAME as $ru => $tr) {
        if (mb_stripos($name, $ru) === 0) {
            $rest = trim(mb_substr($name, mb_strlen($ru)));
            // кольори в дужках
            $rest = str_replace(['(black)','(yellow)','(red)','(gold)'],
                $lang==='ua' ? ['(чорний)','(жовтий)','(червоний)','(золотий)'] : ['(black)','(yellow)','(red)','(gold)'], $rest);
            return trim($tr[$lang] . ' ' . $rest);
        }
    }
    return $name;
}
