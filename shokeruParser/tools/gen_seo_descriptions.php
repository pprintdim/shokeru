<?php
/**
 * Генерація РОЗГОРНУТИХ унікальних описів (RU, HTML) у колонку full_description.
 * Будується з реальних характеристик товару + ротація формулювань (seed=crc32(slug)),
 * обсяг ~200-280 слів: вступ, потужність, конструкція/живлення, призначення, комплект поставки.
 * Оригінальні спарсені описи залишаються в data/products.backup.csv.
 */
require __DIR__ . '/../src/CsvHelper.php';
require __DIR__ . '/../src/ProductParser.php';

$path = __DIR__ . '/../data/products.csv';
$rows = CsvHelper::read($path);

function pick(array $a, int $s): string { return $a[$s % count($a)]; }
function ch(array $c, array $keys): string {
    foreach ($keys as $k) foreach ($c as $ck => $cv) if (mb_stripos($ck, $k) !== false && trim($cv) !== '') return trim($cv);
    return '';
}
function material_phrase(string $m): string {
    $m = mb_strtolower($m);
    if (mb_strpos($m, 'пластик') !== false) return 'из ударопрочного пластика';
    if (mb_strpos($m, 'металл') !== false || mb_strpos($m, 'алюмин') !== false) return 'из прочного металла';
    if (mb_strpos($m, 'комбин') !== false) return 'из комбинированных материалов (металл и пластик)';
    return $m !== '' ? "из материала $m" : '';
}

$done = 0;
foreach ($rows as &$r) {
    $name = trim($r['product_name']);
    $slug = preg_replace('#^https?://[^/]+/#', '', $r['product_url']);
    $seed = crc32($slug);
    $c = (array) json_decode($r['characteristics_json'], true);

    $brand   = ch($c, ['Бренд']);
    $country = ch($c, ['Страна производитель']);
    $year    = preg_replace('/[^0-9]/', '', ch($c, ['Год выпуска']));
    $volt    = ch($c, ['Напряжение']);
    $power   = ch($c, ['Мощность']);
    $cloth   = ch($c, ['Толщина пробоя одежды']);
    $princip = ch($c, ['Принцип действия']);
    $effect  = ch($c, ['Эффективность']);
    $recover = ch($c, ['Время выхода']);
    $mat     = ch($c, ['Материал корпуса']);
    $waterp  = ch($c, ['Влагозащита']);
    $power_src = ch($c, ['Источник питания']);
    $batt_cap  = ch($c, ['Емкость аккумулятора']);
    $charge    = ch($c, ['Тип зарядки']);
    $charges_n = ch($c, ['Количество включений']);
    $dims    = ch($c, ['Габаритные размеры']);
    $weight  = ch($c, ['Вес']);
    $light   = ch($c, ['Дальность света', 'Функция фонарика', 'Ослепляющее']);
    $dog     = ch($c, ['Против собак']);
    $law     = ch($c, ['Разрешение на ношение']);
    $warr    = ch($c, ['Гарантия']);

    $isAccessory = mb_stripos($r['category'], 'ксессуар') !== false
        || preg_match('/аккумулятор|картридж|батаре|заряд|шнур|чехол|кобура|комплект/iu', $name);

    $P = [];

    if ($isAccessory) {
        $aIntro = [
            "%N% — оригинальное комплектующее, которое обеспечивает стабильную работу вашего устройства и продлевает срок его службы.",
            "Перед вами %N% — совместимый аксессуар для надёжной и долгой эксплуатации электрошокера.",
            "%N% — качественная и проверенная комплектующая, подходящая для штатной замены без потери характеристик.",
        ];
        $line = str_replace('%N%', $name, pick($aIntro, $seed));
        $compat = ch($c, ['Совместимость']);
        if ($compat) $line .= " Совместимость: $compat.";
        if ($country) $line .= " Страна-производитель — $country.";
        if ($warr) $line .= " На изделие распространяется официальная гарантия $warr.";
        $P[] = "<p>$line</p>";
    } else {
        // --- Абзац 1: модель, рік, виробник, країна, клас ---
        $maker = $brand ? "компании «{$brand}»" : ($country ? "производителя из страны {$country}" : 'известного производителя');
        $yr = $year ? "$year года" : 'актуального года выпуска';
        $i1 = [
            "Электрошокер «%N%» — модель %YR% от %MK%.",
            "Перед вами «%N%» — современный электрошоковый прибор %YR% производства %MK%.",
            "«%N%» — это надёжное средство самообороны %YR%, выпущенное %MK%.",
        ];
        $s1 = str_replace(['%N%', '%YR%', '%MK%'], [$name, $yr, $maker], pick($i1, $seed));
        if ($country && $brand) $s1 .= " Сборка осуществляется в стране $country, что гарантирует контроль качества.";
        if ($effect) $s1 .= " Устройство относится к средствам $effect и рассчитано на быструю нейтрализацию угрозы.";
        $P[] = "<p>$s1</p>";

        // --- Абзац 2: потужність ---
        $b2 = [];
        if ($volt) $b2[] = str_replace('%V%', $volt, pick([
            'Сила разряда достигает %V%, чего с запасом хватает для остановки даже физически крупного противника.',
            'Напряжение разряда составляет %V% — этого достаточно, чтобы мгновенно дезориентировать нападающего.',
            'Электрический импульс мощностью %V% вызывает кратковременный паралич мышц и сильную боль.',
        ], $seed + 1));
        if ($power) $b2[] = "Полезная мощность устройства — $power.";
        if ($cloth) $b2[] = "Разряд пробивает одежду толщиной до $cloth, поэтому прибор сохраняет эффективность даже зимой.";
        if ($princip) $b2[] = "Принцип действия — $princip: электроды нужно плотно прижать к телу агрессора.";
        if ($recover) $b2[] = "Время выхода из шокового состояния — около $recover, чего достаточно, чтобы уйти в безопасное место.";
        if ($b2) $P[] = '<p>' . implode(' ', $b2) . '</p>';

        // --- Абзац 3: конструкція та живлення ---
        $b3 = [];
        $mp = material_phrase($mat);
        if ($mp) $b3[] = "Корпус выполнен $mp и удобно лежит в руке.";
        if ($waterp && mb_stripos($waterp, 'есть') !== false) $b3[] = 'Предусмотрена защита от влаги, что повышает надёжность в любую погоду.';
        if ($power_src) $b3[] = "Источник питания — $power_src" . ($batt_cap ? " ёмкостью $batt_cap" : '') . '.';
        if ($charge) $b3[] = "Зарядка осуществляется через $charge.";
        if ($charges_n) $b3[] = "Одного заряда хватает на $charges_n включений.";
        if ($dims || $weight) $b3[] = trim(($dims ? "Габариты — $dims" : '') . ($dims && $weight ? ', ' : '') . ($weight ? "вес — $weight" : '')) . '. Прибор компактен и легко помещается в карман или сумку.';
        if ($light) $b3[] = 'Встроенный фонарик пригодится в тёмное время суток и дополнительно ослепляет нападающего.';
        if ($b3) $P[] = '<p>' . implode(' ', $b3) . '</p>';

        // --- Абзац 4: призначення ---
        $b4 = [];
        if ($dog) $b4[] = 'Модель эффективна против агрессивных собак — пригодится курьерам, велосипедистам и любителям вечерних прогулок.';
        if ($law) $b4[] = (mb_stripos($law, 'не') !== false)
            ? 'Для ношения данной модели не требуется разрешение — это разрешённое средство самообороны.'
            : "Разрешение на ношение: $law.";
        $b4[] = pick([
            'Электрошокер станет надёжным спутником для прогулок, поездок и повседневной самозащиты.',
            'Это разумный выбор для тех, кто заботится о личной безопасности себя и близких.',
            'Прибор подойдёт как мужчинам, так и женщинам в качестве компактного средства защиты.',
        ], $seed + 2);
        $P[] = '<p>' . implode(' ', $b4) . '</p>';
    }

    // --- Характеристики списком ---
    $specKeys = $isAccessory
        ? ['Бренд','Страна производитель','Совместимость','Тип аккумулятора','Емкость аккумулятора','Габаритные размеры','Вес','Гарантия']
        : ['Бренд','Страна производитель','Год выпуска','Напряжение','Мощность','Принцип действия','Толщина пробоя одежды','Материал корпуса','Влагозащита','Источник питания','Емкость аккумулятора','Габаритные размеры','Вес','Гарантия'];
    $li = [];
    foreach ($specKeys as $k) { $v = ch($c, [$k]); if ($v !== '') $li[] = "<li><strong>$k:</strong> $v</li>"; }
    if ($li) { $P[] = '<h4>Технические характеристики «' . $name . '»:</h4>'; $P[] = '<ul>' . implode('', array_slice($li, 0, 12)) . '</ul>'; }

    // --- Комплект поставки ---
    if ($isAccessory) {
        $P[] = "<h4>В комплект поставки входит:</h4>\n<ul><li>" . $name . "</li><li>Упаковка</li><li>Гарантийный талон</li></ul>";
    } else {
        $kit = ['Электрошокер «' . $name . '»', 'Чехол для ношения', 'Зарядное устройство', 'Инструкция по эксплуатации', 'Гарантийный талон', 'Подарочная упаковка'];
        $P[] = "<h4>В комплект поставки входит:</h4>\n<ul>" . implode('', array_map(fn($x) => "<li>$x</li>", $kit)) . "</ul>";
    }

    // --- CTA ---
    $P[] = '<p>' . str_replace('%N%', $name, pick([
        'Заказать «%N%» можно с доставкой по всей Украине — отправка в день оформления заказа Новой Почтой.',
        'Купить «%N%» по выгодной цене с быстрой доставкой и проверкой при получении вы можете прямо сейчас.',
        'Оформите заказ на «%N%» — поможем с выбором, проконсультируем и оперативно отправим в ваш город.',
    ], $seed + 3)) . '</p>';

    $r['full_description'] = implode("\n", $P);
    $done++;
}
unset($r);

CsvHelper::write($path, $rows, ProductParser::COLUMNS);
$words = str_word_count(strip_tags($rows[5]['full_description']));
echo "Перегенеровано full_description: $done товарів\n";
